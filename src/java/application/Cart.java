
package application;


public class Cart {
    private int id;
    private double totalCart;
    
    
    public Cart(int id, double total){
        this.id = id;
        this.totalCart = total;
    }

    public int getId() {
        return id;
    }

    public double getTotalCart() {
        return totalCart;
    }

    

   
    
    
}
